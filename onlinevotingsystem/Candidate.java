    private void CandCount(){
        try{
           st1 = con.createStatement();
           rs1 = st1.executeQuery("SELECT MAX(CId) FROM Candidate_table");
           rs1.next();
           CId = rs1.getInt(1) + 1;
        }catch(Exception e){
            System.out.println(e);
        }
    }
     
    private void AddBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddBtnMouseClicked
        // TODO add your handling code here:
        if(CAgeTb.getText().isEmpty() || CandNameTb.getText().isEmpty() || ElectionCb.getSelectedIndex() == -1 || CandGenTb.getSelectedIndex()==-1)
        {
            JOptionPane.showMessageDialog(this, "Missing Information");
        }else{
            
            try{
                CandCount();
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/onlinevotingsystem", "root", "");
                PreparedStatement Add = con.prepareStatement("INSERT INTO Candidate_table VALUES (?,?,?,?,?,?)");
                InputStream img = new FileInputStream(imgpath);
                Add.setInt(1, CId);
                Add.setString(2,CandNameTb.getText());
                Add.setInt(3, Integer.valueOf(CAgeTb.getText()));
                Add.setString(4,CandGenTb.getSelectedItem().toString());
                Add.setBlob(5, img);
                Add.setInt(6, Integer.valueOf(ElectionCb.getSelectedItem().toString()));
                int row = Add.executeUpdate();
                JOptionPane.showMessageDialog(this, " Candidate Registered successfully");
                con.close();
                DisplayCandidates();
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_AddBtnMouseClicked

    
